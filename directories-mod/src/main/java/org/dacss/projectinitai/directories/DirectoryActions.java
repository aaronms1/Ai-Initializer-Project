package org.dacss.projectinitai.directories;

/**
 * <h1>{@link DirectoryActions}</h1>
 * Enum representing various directory actions.
 * This enum defines the different types of actions that can be performed on directories and files.
 */
public enum DirectoryActions {
    /**
     * Action to create a directory.
     */
    CREATE_DIRECTORY,

    /**
     * Action to delete a directory.
     */
    DELETE_DIRECTORY,

    /**
     * Action to create a file.
     */
    CREATE_FILE,

    /**
     * Action to delete a file.
     */
    DELETE_FILE
}
